package listaDois;

public class questao16 {
    public static int soma(int a, int b, int c) {
        return a + b + c;}

    public static void main(String[] args) {
        int numero1 = 5;
        int numero2 = 10;
        int numero3 = 15;

        int resultado = soma(numero1, numero2, numero3);
        System.out.println("A soma dos três números é: " + resultado);
    }
}
